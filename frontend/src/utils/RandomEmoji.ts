const usedEmojis: Set<string> = new Set();

export const getRandomUniqueEmoji = (): string | null => {
  if (usedEmojis.size === emojis.length) {
    console.warn("All emojis have been used.");
    return null;
  }

  let emoji: string;
  do {
    const randomIndex = Math.floor(Math.random() * emojis.length);
    emoji = emojis[randomIndex];
  } while (usedEmojis.has(emoji));

  usedEmojis.add(emoji);
  return emoji;
};
